#!/usr/bin/env python3
"""
Cross-check English province spellings across the four MIT-licensed input sources.

Joins on Thai script (the language-independent key), compares the English-name column,
and reports per-source disagreements with named source attribution.

Reproduces the 2026-05-06 finding: 76 of 77 provinces agree across all four sources;
one disagreement on ลพบุรี (Loburi vs. Lopburi).

Inputs:
    data/inputs/thailand-geography-data/provinces.json
    data/inputs/kongvut/province.json
    data/inputs/geothai/provinces_v1.json
    data/inputs/geothai/provinces_v2.json

Output:
    data/v0.1.0/cross_check_report.md

Usage:
    python3 bin/cross_check_inputs.py
"""

import json
from pathlib import Path
from collections import defaultdict

ROOT = Path(__file__).resolve().parent.parent
INPUTS = ROOT / "data" / "inputs"
OUTPUT = ROOT / "data" / "v0.1.0" / "cross_check_report.md"


def load_sources():
    """Return a dict {source_name: {thai_script: english_name}} for all four inputs."""
    with open(INPUTS / "thailand-geography-data" / "provinces.json", encoding="utf-8") as f:
        tgd = json.load(f)
    with open(INPUTS / "kongvut" / "province.json", encoding="utf-8") as f:
        kongvut = json.load(f)
    with open(INPUTS / "geothai" / "provinces_v1.json", encoding="utf-8") as f:
        gth1 = json.load(f)
    with open(INPUTS / "geothai" / "provinces_v2.json", encoding="utf-8") as f:
        gth2 = json.load(f)

    return {
        "thailand-geography-data": {
            r["provinceNameTh"].strip(): r["provinceNameEn"].strip() for r in tgd
        },
        "kongvut": {
            r["name_th"].strip(): r["name_en"].strip() for r in kongvut
        },
        "geothai_v1": {
            r["province_name_th"].strip(): r["province_name_en"].strip() for r in gth1
        },
        "geothai_v2": {
            r["th"].strip(): r["en"].strip() for r in gth2
        },
    }


def cross_check(sources):
    """Return (agree_count, disagreements_list, all_thai_keys)."""
    all_thai = sorted(set().union(*[set(s.keys()) for s in sources.values()]))
    disagreements = []
    agree_count = 0

    for thai in all_thai:
        spellings = defaultdict(list)
        for src_name, src_map in sources.items():
            if thai in src_map:
                spellings[src_map[thai]].append(src_name)
        if len(spellings) > 1:
            disagreements.append((thai, dict(spellings)))
        elif len(spellings) == 1:
            agree_count += 1

    return agree_count, disagreements, all_thai


def write_report(agree_count, disagreements, all_thai, sources):
    """Write a human-readable markdown report."""
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)

    lines = []
    lines.append("# Input Cross-Check Report — Province English Spellings")
    lines.append("")
    lines.append(
        "Generated by `bin/cross_check_inputs.py`. Joins on Thai script (language-independent key)."
    )
    lines.append("")
    lines.append("## Source coverage")
    lines.append("")
    lines.append("| Source | Provinces |")
    lines.append("|---|---|")
    for src_name, src_map in sources.items():
        lines.append(f"| `{src_name}` | {len(src_map)} |")
    lines.append("")
    lines.append(f"Unique Thai-script keys across all sources: {len(all_thai)}")
    lines.append("")
    lines.append("## Cross-check result")
    lines.append("")
    lines.append("| Outcome | Count |")
    lines.append("|---|---|")
    lines.append(f"| All sources agree on English spelling | {agree_count} of {len(all_thai)} |")
    lines.append(f"| At least one source disagrees | {len(disagreements)} |")
    lines.append("")

    if disagreements:
        lines.append("## Disagreements")
        lines.append("")
        for thai, spellings in disagreements:
            lines.append(f"### {thai}")
            lines.append("")
            for spelling, srcs in spellings.items():
                lines.append(f"- `{spelling}`  ←  {', '.join(srcs)}")
            lines.append("")
    else:
        lines.append("No spelling disagreements found across the input sources.")
        lines.append("")

    OUTPUT.write_text("\n".join(lines))
    print(f"Report written to: {OUTPUT}")


def main():
    sources = load_sources()
    agree_count, disagreements, all_thai = cross_check(sources)

    print(f"Sources loaded: {list(sources.keys())}")
    print(f"Unique Thai-script keys: {len(all_thai)}")
    print(f"All sources agree: {agree_count} of {len(all_thai)}")
    print(f"At least one disagreement: {len(disagreements)}")

    if disagreements:
        print("\nDisagreements:")
        for thai, spellings in disagreements:
            print(f"  {thai}")
            for spelling, srcs in spellings.items():
                print(f"    \"{spelling}\"  <-  {', '.join(srcs)}")

    write_report(agree_count, disagreements, all_thai, sources)


if __name__ == "__main__":
    main()
